package Question10;

public class Course {
 
	int cid;
	String cname;
	public Course(int cid, String cname) {
		super();
		this.cid = cid;
		this.cname = cname;
		
		
		
		
	}
	@Override
	public String toString() {
		return "Course [cid=" + cid + ", cname=" + cname + "]";
	}
	
	
}
